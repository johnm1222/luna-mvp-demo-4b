import SwiftUI

struct TodaysSnapshotSnapView: View {
    @Environment(\.dismiss) private var dismiss
    @State private var currentIndex = 0
    
    private var formattedDate: String {
        let formatter = DateFormatter()
        formatter.dateFormat = "MMMM d"
        return formatter.string(from: Date())
    }
    
    var body: some View {
        GeometryReader { geometry in
            ZStack {
                // Background
                Color("bottomSheetBackgroundDeemphasized")
                    .ignoresSafeArea()
                
                VStack(spacing: 0) {
                    // Navigation Bar
                    HStack {
                        Button(action: { dismiss() }) {
                            Image("chevron-left-outline")
                                .resizable()
                                .renderingMode(.template)
                                .frame(width: 24, height: 24)
                                .foregroundColor(Color("primaryIcon"))
                        }
                        .padding(.leading, 12)
                        
                        Spacer()
                    }
                    .frame(height: 52)
                    .background(Color("surfaceBackground"))
                    
                    // Vertical Snapping ScrollView
                    ScrollViewReader { scrollProxy in
                        ScrollView(.vertical, showsIndicators: false) {
                            LazyVStack(spacing: 0) {
                                // Initial landing page
                                InitialSnapPage(
                                    formattedDate: formattedDate,
                                    currentIndex: $currentIndex,
                                    scrollProxy: scrollProxy,
                                    onExplore: {
                                        withAnimation(.easeInOut(duration: 0.4)) {
                                            currentIndex = 1
                                            scrollProxy.scrollTo(1, anchor: .top)
                                        }
                                    }
                                )
                                .frame(width: geometry.size.width, height: geometry.size.height - 52)
                                .id(0)
                                
                                // Full-screen snapshot cards
                                ForEach(Array(snapSnapshotItems.enumerated()), id: \.element.id) { index, item in
                                    FullScreenSnapCard(item: item)
                                        .frame(width: geometry.size.width, height: geometry.size.height - 52)
                                        .id(index + 1)
                                }
                            }
                            .scrollTargetLayout()
                        }
                        .scrollTargetBehavior(.paging)
                        .scrollIndicators(.hidden)
                    }
                }
            
            // White overlay mask at bottom to prevent peeking
            VStack {
                Spacer()
                Rectangle()
                    .fill(Color.white)
                    .frame(height: 40)
                    .offset(y: 20) // Offset to cover the bottom 60px area
                    .allowsHitTesting(false)
            }
            .ignoresSafeArea()
        }
        .hideFDSTabBar(true)
        .navigationBarHidden(true)
    }
    }
}

