                                Spacer()
                            }
                            .padding(.horizontal, 12)
                            .padding(.top, 12)
                        }
                        
                        // "People are saying" section
                        Text("People are saying")
                            .headline4Typography()
                            .foregroundStyle(Color("primaryText"))
                            .padding(.horizontal, 12)
                            .padding(.top, 20)
                        
                        // 2-column post grid
                        SnapPostGrid(items: item.mediaItems)
                            .padding(.horizontal, 12)
                            .padding(.top, 8)
                        
                        // Thumbs up/down footer
                        HStack(spacing: 16) {
                            Button(action: {}) {
                                Image("hand-thumbs-up-outline")
                                    .resizable()
                                    .renderingMode(.template)
                                    .frame(width: 16, height: 16)
                                    .foregroundColor(Color("secondaryIcon"))
                            }
                            
                            Button(action: {}) {
                                Image("hand-thumbs-down-outline")
                                    .resizable()
                                    .renderingMode(.template)
                                    .frame(width: 16, height: 16)
                                    .foregroundColor(Color("secondaryIcon"))
                            }
                            
                            Spacer()
                        }
                        .padding(.horizontal, 12)
                        .padding(.top, 16)
                        .padding(.bottom, 20)
                    }
                }
            }
    }
}

// MARK: - Snap Post Grid

struct SnapPostGrid: View {
    let items: [MediaItem]
    
    var body: some View {
        LazyVGrid(
            columns: [
                GridItem(.flexible(), spacing: 8),
                GridItem(.flexible(), spacing: 8)
            ],
            spacing: 8
        ) {
            ForEach(Array(items.prefix(4).enumerated()), id: \.offset) { index, item in
                ResponsivePostCard(item: item, index: index)
            }
        }
    }
}

// MARK: - Responsive Post Card

struct ResponsivePostCard: View {
    let item: MediaItem
    let index: Int
    
    var body: some View {
        GeometryReader { geometry in
            ZStack(alignment: .topLeading) {
                // Full background image
                Image(item.imageName)
                    .resizable()
                    .scaledToFill()
                    .frame(width: geometry.size.width, height: geometry.size.height)
                    .clipped()
                
                // Header overlaid on top
                HStack(spacing: 8) {
                    Circle()
                        .fill(Color.white.opacity(0.9))
                        .frame(width: 20, height: 20)
                        .overlay(
                            Text("BT")
                                .font(.system(size: 10, weight: .semibold))
                                .foregroundColor(Color("primaryText"))
                        )
                    
                    Text("Becker Threads")
                        .font(.system(size: 13, weight: .semibold))
                        .foregroundColor(Color.white)
                        .kerning(-0.08)
                        .shadow(color: Color.black.opacity(0.3), radius: 2, x: 0, y: 1)
                    
                    Spacer()
                }
                .padding(.horizontal, 12)
                .padding(.top, 12)
            }
            .frame(width: geometry.size.width)
            .cornerRadius(12)
            .clipped()
        }
        .aspectRatio(0.7, contentMode: .fit)
    }
}

