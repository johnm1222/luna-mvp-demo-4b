                            }
                        }
                        .padding(.horizontal, 12)
                        .padding(.top, 12)
                        
                        // Summary paragraph with see more
                        Button(action: {
                            withAnimation {
                                isExpanded.toggle()
                            }
                        }) {
                            VStack(alignment: .leading, spacing: 0) {
                                if isExpanded {
                                    Text(item.summaryParagraph)
                                        .body3Typography()
                                        .foregroundStyle(Color("primaryText"))
                                        .frame(maxWidth: .infinity, alignment: .leading)
                                } else {
                                    (Text(item.summaryParagraph)
                                        .foregroundColor(Color("primaryText"))
                                    + Text("... ")
                                        .foregroundColor(Color("primaryText"))
                                    + Text("see more")
                                        .foregroundColor(Color("secondaryText")))
                                    .body3Typography()
                                    .lineLimit(2)
                                    .frame(maxWidth: .infinity, alignment: .leading)
                                }
                            }
                        }
                        .buttonStyle(PlainButtonStyle())
                        .padding(.horizontal, 12)
                        .padding(.top, 8)
                        
                        // Sources action chip (only when expanded)
                        if isExpanded {
                            HStack {
                                FDSActionChip(
                                    surface: .surface,
                                    type: .primary,
                                    size: .medium,
                                    label: "Sources",
                                    leftAddOn: nil,
                                    isMenu: false,
                                    isEmphasized: false,
